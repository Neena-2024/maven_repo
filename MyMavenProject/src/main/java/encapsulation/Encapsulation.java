package encapsulation;

public class Encapsulation {

	private String name;
	private int age;  //click the getter and setter on age
	public int getAge() { //method is used to get value
		return age;
	}
	public void setAge(int age) { // method is used to get value
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
